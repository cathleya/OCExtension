+ (BOOL)isJailBroken
{
	for (int i = 0; jailbreak_apps[i] != NULL; ++i)
	{
		//检测应用路径的方式来判断是否越狱
		if ([[NSFileManager defaultManager] fileExistsAtPath:[NSString stringWithUTF8String:jailbreak_apps[i]]])
		{
			return YES;
		}		
	}
    
    // method 2
	if ( [[NSFileManager defaultManager] fileExistsAtPath:@"/private/var/lib/apt/"] )
	{
		return YES;
	}
	
	return NO;
}

